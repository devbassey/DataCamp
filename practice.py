# BMI CALCULATOR

height = 60
weight = 160
bmi = weight / (height ** 2)
print(bmi)

if bmi < 18.5:
    print("Underweight")
elif bmi < 24.9:
    print("Normal weight")
elif bmi < 29.9:
    print("Overweight")
else:
    print("Obesity")

print("BMI Category")
print("Underweight: <18.5")
